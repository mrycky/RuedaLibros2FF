    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
       DAOUsers dao = new DAOUsersImpl();  
       DefaultTableModel model = (DefaultTableModel) TableUsuario.getModel();
       if(TableUsuario.getSelectedRows().length<1){
        javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar uno o más usuarios a eliminar \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);

       }else{
        for(int i:TableUsuario.getSelectedRows()){
            try{
            int userId = (int)TableUsuario.getValueAt(i, 0);  
             dao.eliminar((int)TableUsuario.getValueAt(i, 0));
            model.removeRow(i);
           }catch(Exception e){
                System.out.println(e.getMessage());
        }
       }
        
       }
    }//GEN-LAST:event_btnBorrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
       if(TableUsuario.getSelectedRow() > -1){
           try{
            int userId = (int) TableUsuario.getValueAt(TableUsuario.getSelectedRow(), 0);
            DAOUsers dao = new DAOUsersImpl();
            Ventana.ShowJPanel(new SubirUsuario(dao.getusuariobById(userId)));
           }catch(Exception e){
               System.out.println(e.getMessage());
           }
           
       }else{
           javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar el ususario a editar. \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
       } 
    }//GEN-LAST:event_btnEditarActionPerformed

