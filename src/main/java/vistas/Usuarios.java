    private void CargarUsuario(){
        try{
           DAOUsers dao = new DAOUsersImpl(); 
           DefaultTableModel model = (DefaultTableModel) TableUsuario.getModel(); 
                    
           dao.listar("").forEach((u) -> model.addRow(new Object[] {u.getId(),u.getNombre(), u.getApellido1(), u.getApellido2(), u.getCedula(), 
                u.getEdad(), u.getEstado_civil(), u.getDomicilio(), u.getTelefono(), u.getSancion(), u.getSancion_dinero()}));       
        }catch(Exception e){
            System.out.println(e.getMessage());
        }
    }
